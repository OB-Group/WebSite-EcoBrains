      <p class="ecobrainsParagraph">Muitas das vezes depois daquela reforma ou com a vinda de novos móveis
        fica aquela dúvida de onde deixar o seu armário. Quase sempre esse resíduo vai parar nos rios ou
        terrenos baldios criando uma série de problemas para as pessoas que vivem no entorno. Para a solução
        desse problema algumas prefeituras criaram os PEV's (Pontos de Entrega Voluntária) que são os locais
        adequados para o descarte desses armários, móveis e entulhos. Para realizar o descarte é só se
        dirigir a um dos PEV's instalados e depositar o resíduo gratuitamente.<br>
        É importante ressaltar que cada cidade tem seu regimento sobre quanto cada morador pode levar por
        dia. Para saber mais acesse o site da sua prefeitura.</p>
